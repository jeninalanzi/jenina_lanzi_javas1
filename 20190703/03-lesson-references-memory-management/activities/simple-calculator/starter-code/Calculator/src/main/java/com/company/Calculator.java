package com.company;

public class Calculator {

    // Default constructor
    public Calculator() {}

    // Calculator methods
    public static int add(int a, int b) {
        return a+b;
    }

    public static int subtract(int a, int b) {
        return a-b;
    }

    public static int multiply(int a, int b) {
        return a*b;
    }

    public static int divide(int a, int b) {
        return a/b;
    }
}
